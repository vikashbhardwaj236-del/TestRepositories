package Day4_PrimitiveDataTypes;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		// Variables - Container that hold the values / it change during the compilation
		
		
	byte a =17;
	System.out.println(a);
	
	short b; //initialization or declaration
	b = 20; // assignments
	System.out.println(b);
	
	int c = 309999999;
	System.out.println(c);
	
	long d = 40000000000000000L;
	System.out.println(d);
	
	char ch = 'V';
	System.out.println(ch);
	
	float weight = 2.598F;
	System.out.println(weight);
	
	double d1 = 4.587790;
	System.out.println(d1);
	
	boolean b1 = true;
	System.out.println(b1);
	boolean b2 = false;
	System.out.println(b2);
	
	var t = 'S';
	
	System.out.println("It select data type automatically account to variable value : "+  t);
			
	
	
	
	

	}

}
