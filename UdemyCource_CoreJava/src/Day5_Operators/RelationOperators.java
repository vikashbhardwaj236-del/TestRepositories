package Day5_Operators;

public class RelationOperators {

	public static void main(String[] args) 
	
	{
		/*
		Relational operators in Java are used to compare two values. They return a boolean result: either true or false.
		Like : 
		> Greater
		>= Greater Equal
		< less than
		<= less than equal
		!= not equal
		== equal 		
		 */

	// Less than
	int a = 10;
	int b = 20;
	boolean result = a<b;
	System.out.println(result);
		
		
	// Greater Than	
	 int c = 10;
		int d = 20;
		boolean Rst = c>d;
		System.out.println(Rst);
		
	//Less than and Equal
		int e = 11;
		int f = 10;
		boolean ef = e<=f;
		System.out.println(ef); 
	
	//	GreaterThan Equal 
		int g = 15;
		int h = 10;
		boolean gh = g>=h;
		System.out.println(gh);
		
	// NOt Equal (!=)
		int i = 20;
		int j = 25;
		boolean ij = i!=j;
		System.out.println(ij);
		
	//	Equal (==)
	   int k = 30;
	   int l = 35;
	   boolean kl = k==l;
	   System.out.println(kl);

	   
	   
		

		
		
		
		
		
		
		
		
		
	}

}
