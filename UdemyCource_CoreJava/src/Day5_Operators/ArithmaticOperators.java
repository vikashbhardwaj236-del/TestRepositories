package Day5_Operators;

public class ArithmaticOperators {

	public static void main(String[] args) 
	{
		/* Arithmetic operators in Java are used to perform basic mathematical operations 
		like addition, subtraction, multiplication, division, and finding the remainder.
		Arithmetic operators -> +,-,*,/,% */

		//Addition
		int a = 50;
		int b = 25;
		int Add = a+b;
		System.out.println("Addition of a and b is : "+ Add);
		
		
		
		//Subtraction
		int c = 40;
		int d = 20;
		int Sub = c-d;
		System.out.println("Subtraction of c and d is : "+ Sub);
				
		// Multiplication 
		int e = 15;
		int f = 5;
		int mul = e*f;
		System.out.println("Multiplication of e and f is : "+ mul);
		
		
		//Division
		int g = 80;
		int h = 10;
		int div = g/h;
		System.out.println("Division of g and h is : "+div);
		
		//Modulus(Reminder)
		int i = 53;
		int j = 5;
		int Reminder = i%j;
		System.out.println("Reminder of the i and j is : " + Reminder);
		
		
		
				
				
				
		
	}

}
